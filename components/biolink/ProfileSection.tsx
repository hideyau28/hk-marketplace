/** Build the social links to render — use socialLinks if available, fallback to legacy instagram field */
function resolveSocialLinks(tenant: TenantForBioLink): SocialLink[] {
  if (tenant.socialLinks && tenant.socialLinks.length > 0) {
    return tenant.socialLinks.slice(0, 4);
  }
  // Backward compat: show Instagram icon from legacy field
  if (tenant.instagram) {
    return [
      {
        platform: "instagram",
        url: `https://instagram.com/${tenant.instagram.replace("@", "")}`,
      },
    ];
  }
  return [];
}

export default function ProfileSection({ tenant }: Props) {
  const tmpl = useTemplate();
  const fallbackLetter = getAvatarFallback(tenant);
  const color = tenant.brandColor || tmpl.accent;
  const links = resolveSocialLinks(tenant);

  return (
    <div className="relative -mt-12 px-5 pb-4">
      {/* Avatar */}
      <div
        className="w-20 h-20 rounded-full border-[3px] overflow-hidden mx-auto mb-3"
        style={{ borderColor: tmpl.bg, boxShadow: `0 0 20px ${color}40` }}
      >
        {tenant.logoUrl ? (
          <Image
            src={tenant.logoUrl}
            alt={tenant.name}
            width={80}
            height={80}
            className="w-full h-full object-cover"
          />
        ) : (
          <div
            className="w-full h-full flex items-center justify-center text-2xl font-bold text-white"
            style={{ backgroundColor: color }}
          >
            {fallbackLetter}
          </div>
        )}
      </div>

      {/* Name */}
      <h1 className="text-lg font-bold text-center" style={{ color: tmpl.text, fontFamily: tmpl.headingFont }}>{tenant.name}</h1>

      {/* Tagline */}
      {tenant.description && (
        <p className="text-sm text-center mt-1 line-clamp-2" style={{ color: tmpl.subtext }}>
          {tenant.description}
        </p>
      )}

      {/* Social links — icon row */}
      {links.length > 0 && (
        <div className="flex justify-center gap-3 mt-3">
          {links.map((link, i) => (
            <a
              key={`${link.platform}-${i}`}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className="w-8 h-8 rounded-full flex items-center justify-center transition-opacity hover:opacity-70"
              style={{ backgroundColor: `${tmpl.subtext}20`, color: tmpl.subtext }}
              aria-label={link.platform}
            >
              <SocialIcon platform={link.platform} className="w-4 h-4" />
            </a>
          ))}
        </div>
      )}

    </div>
  );
}
